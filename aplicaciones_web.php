        <section>
            <h2>Frontend (o interfaz de usuario)</h2>
            <p>El frontend es todo lo que un usuario ve e interactúa en una aplicación web o sitio web. Es la cara visible del producto, la parte con la que el usuario tiene contacto directo. Piensa en él como la interfaz de un teléfono celular: todo lo que tocas y ves.</p>
            <p><strong>Ejemplo:</strong> Los botones, los menús, las imágenes, los formularios y el diseño general de una página web son parte del frontend.</p>
        </section>
        
        <section>
            <h3>Tecnologías frontend: HTML, CSS, JS</h3>
            <ul>
                <li><strong>HTML (HyperText Markup Language):</strong> Es el lenguaje estándar para estructurar el contenido de una página web. Define la estructura básica de una página, como los encabezados, párrafos, listas y enlaces.</li>
                <li><strong>CSS (Cascading Style Sheets):</strong> Se utiliza para dar estilo a las páginas web. Define cómo se verá el contenido, controlando aspectos como colores, fuentes, diseño y posicionamiento de los elementos.</li>
                <li><strong>JavaScript:</strong> Es un lenguaje de programación que agrega interactividad a las páginas web. Permite crear efectos dinámicos, validar formularios, manipular el contenido de la página y mucho más.</li>
            </ul>
        </section>
        
        <section>
            <h2>Backend (o lógica del servidor)</h2>
            <p>El backend es la parte de una aplicación que los usuarios no ven directamente. Se encarga de la lógica de la aplicación, el procesamiento de datos, la gestión de la base de datos y la interacción con otros sistemas. Es como el motor de un coche, que hace que todo funcione sin que el conductor lo note.</p>
            <p><strong>Ejemplo:</strong> Cuando envías un formulario en una página web, el backend se encarga de procesar esa información, guardarla en una base de datos y posiblemente enviar una respuesta al usuario.</p>
        </section>
        
        <section>
            <h3>Tecnologías backend: Base de datos, Servidor web, Lenguajes de programación más utilizados</h3>
            <ul>
                <li><strong>Base de datos:</strong> Un sistema organizado para almacenar, gestionar y recuperar grandes cantidades de datos. Las bases de datos son fundamentales para almacenar la información de los usuarios, productos, pedidos, etc.</li>
                <li><strong>Servidor web:</strong> Un programa informático que sirve contenido (páginas web, imágenes, videos, etc.) a través de una red (generalmente Internet).</li>
                <li><strong>Lenguajes de programación más utilizados:</strong>
                    <ul>
                        <li><strong>Python:</strong> Conocido por su sintaxis sencilla y legibilidad, es ampliamente utilizado en el desarrollo web, machine learning y data science.</li>
                        <li><strong>JavaScript (también en backend):</strong> Con Node.js, JavaScript se puede utilizar tanto en el frontend como en el backend.</li>
                        <li><strong>Java:</strong> Un lenguaje robusto y orientado a objetos, utilizado en grandes aplicaciones empresariales.</li>
                        <li><strong>PHP:</strong> Uno de los lenguajes más populares para el desarrollo web, especialmente para sitios dinámicos.</li>
                        <li><strong>Ruby:</strong> Con Ruby on Rails, es un framework web muy popular por su productividad y convenciones.</li>
                        <li><strong>C#:</strong> Principalmente utilizado para desarrollar aplicaciones .NET, pero también se puede utilizar en el desarrollo web.</li>
                    </ul>
                </li>
            </ul>
        </section>
        
        <section>
            <h2>FullStack</h2>
            <p>Un desarrollador fullstack es aquel que tiene conocimientos y habilidades tanto en frontend como en backend. Esto significa que pueden trabajar en todas las capas de una aplicación, desde la interfaz de usuario hasta la base de datos. Los desarrolladores fullstack son muy valorados en la industria porque pueden comprender todo el ciclo de desarrollo de una aplicación y resolver problemas en diferentes niveles.</p>
